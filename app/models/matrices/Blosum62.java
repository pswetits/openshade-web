	private static String aminoAcids = "ARNDCQEGHILKMFPSTWYVBZX*";
	
	private int lowRange = -4;
	private int highRange = 11;
	
	/**
	 * returns score of two amino acids
	 */
	public static int getScore(String consensus, String current){
		if(consensus.equals('-') || current.equals("-"))
			return -1;
		
		int x = aminoAcids.indexOf(consensus.charAt(0));
		int y = aminoAcids.indexOf(current.charAt(0));
		
		return blosum62[x][y];
	}
	
	/**
	 * returns low range of matrix
	 */
